package com.company;

public class Person {
    String name;
    int age;

    public Person(String name, int age) {
        name = name;
        age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setName(String name) {
        name = name;
    }

    public void setAge(int age) {
        age = age;
    }
}
